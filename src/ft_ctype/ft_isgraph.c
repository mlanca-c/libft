/* This function checks whether the passed character has graphical
 * representation using locale. */
int	ft_isgraph(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9') || (c >= '!' && c <= '/')
		|| (c >= ':' && c <= '@') || (c >= '[' && c <= '`')
		|| (c >= '{' && c <= '~'));
}
