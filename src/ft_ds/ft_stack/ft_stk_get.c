#include "ft_ds.h"

int	ft_stk_get(t_stk *stk, int pos)
{
	int	i;

	i = 0;
	while (stk)
	{
		if (i == pos)
			return (stk->data);
		stk = stk->next;
		i++;
	}
	return (INT_MIN);
}
